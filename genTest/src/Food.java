
public class Food {
    private int meat = 10;
    private boolean fish = true;
    private String corn = "f";

    public Food() {
    }

    public int getMeat() {
        return meat;
    }

    public void setMeat(int meat) {
        this.meat = meat;
    }

    public boolean getFish() {
        return fish;
    }

    public void setFish(boolean fish) {
        this.fish = fish;
    }

    public String getCorn() {
        return corn;
    }

    public void setCorn(String corn) {
        this.corn = corn;
    }




}
